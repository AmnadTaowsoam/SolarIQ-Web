import fs from 'fs'
import path from 'path'
import prettier from 'prettier'

const rootDir = process.cwd()
const blogDirectory = path.join(rootDir, 'content', 'blog')
const outputPath = path.join(rootDir, 'src', 'generated', 'blog-posts.ts')

function stripQuotes(value) {
  return value.trim().replace(/^['"]|['"]$/g, '')
}

function decodePossiblyMojibake(value) {
  if (/[\u0E00-\u0E7F]/.test(value) || !/[ÃÂ]/.test(value)) {
    return value
  }

  try {
    const decoded = Buffer.from(value, 'latin1').toString('utf8')
    return decoded.includes('ï¿½') ? value : decoded
  } catch {
    return value
  }
}

function parseTags(value) {
  const normalized = value.trim()
  if (!normalized.startsWith('[') || !normalized.endsWith(']')) {
    return []
  }

  return normalized
    .slice(1, -1)
    .split(',')
    .map((tag) => stripQuotes(tag))
    .filter(Boolean)
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/)
  if (!match?.[1]) {
    return {}
  }

  return match[1].split('\n').reduce((acc, line) => {
    const separatorIndex = line.indexOf(':')
    if (separatorIndex === -1) {
      return acc
    }

    const key = line.slice(0, separatorIndex).trim()
    const value = line.slice(separatorIndex + 1).trim()
    if (key) {
      acc[key] = value
    }
    return acc
  }, {})
}

function getAllBlogPosts() {
  const filenames = fs
    .readdirSync(blogDirectory)
    .filter((filename) => filename.endsWith('.md'))
    .sort()

  return filenames
    .map((filename) => {
      const filePath = path.join(blogDirectory, filename)
      const raw = decodePossiblyMojibake(fs.readFileSync(filePath, 'utf-8'))
      const frontmatter = parseFrontmatter(raw)
      const content = decodePossiblyMojibake(raw.replace(/^---\n[\s\S]*?\n---\n?/, '').trim())

      const slug = stripQuotes(decodePossiblyMojibake(frontmatter.slug || ''))
      const title = stripQuotes(decodePossiblyMojibake(frontmatter.title || ''))
      const excerpt = stripQuotes(decodePossiblyMojibake(frontmatter.excerpt || ''))
      const category = stripQuotes(decodePossiblyMojibake(frontmatter.category || ''))
      const author = stripQuotes(decodePossiblyMojibake(frontmatter.author || 'SolarIQ Team'))
      const date = stripQuotes(frontmatter.date || '')
      const readTime = stripQuotes(decodePossiblyMojibake(frontmatter.readTime || ''))
      const tags = parseTags(decodePossiblyMojibake(frontmatter.tags || ''))

      if (!slug || !title || !date) {
        return null
      }

      return {
        slug,
        title,
        excerpt,
        category,
        author,
        date,
        readTime,
        tags,
        content,
      }
    })
    .filter(Boolean)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
}

async function writeBlogPosts(posts) {
  const source = `/* eslint-disable */
// This file is auto-generated by scripts/generate-blog-data.mjs.
import type { BlogPost } from '@/lib/blog-schema'

export const blogPosts: BlogPost[] = ${JSON.stringify(posts, null, 2)}
`
  const prettierConfig = (await prettier.resolveConfig(outputPath)) ?? {}
  const formattedSource = await prettier.format(source, {
    ...prettierConfig,
    filepath: outputPath,
    parser: 'typescript',
  })

  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, formattedSource)
}

await writeBlogPosts(getAllBlogPosts())
