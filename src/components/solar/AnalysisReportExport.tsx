export function AnalysisReportExport({ result }: AnalysisReportExportProps) {
  const t = useTranslations('analysisReport')
  const [copied, setCopied] = useState(false)

  const handlePrintReport = () => {
    const html = generatePrintHtml(result)
    const printWindow = window.open('', '_blank')
    if (printWindow) {
      printWindow.document.write(html)
      printWindow.document.close()
    }
  }

  const handleCopyReport = async () => {
    const text = generateTextSummary(result)
    try {
      await navigator.clipboard.writeText(text)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    } catch {
      // Fallback for older browsers
      const textarea = document.createElement('textarea')
      textarea.value = text
      textarea.style.position = 'fixed'
      textarea.style.opacity = '0'
      document.body.appendChild(textarea)
      textarea.select()
      document.execCommand('copy')
      document.body.removeChild(textarea)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    }
  }

  return (
    <div className="flex flex-wrap gap-2">
      <Button
        variant="primary"
        size="sm"
        onClick={handlePrintReport}
        leftIcon={<Download className="w-4 h-4" />}
      >
        {t('downloadPdf')}
      </Button>
      <Button
        variant="outline"
        size="sm"
        onClick={handleCopyReport}
        leftIcon={
          copied ? <Check className="w-4 h-4 text-green-500" /> : <Copy className="w-4 h-4" />
        }
      >
        {copied ? t('exporting') : t('shareReport')}
      </Button>
    </div>
  )
}
