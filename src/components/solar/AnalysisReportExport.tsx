const formatCurrency = (value: number): string =>
  new Intl.NumberFormat('th-TH', {
    style: 'currency',
    currency: 'THB',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(value)

const formatNumber = (value: number, decimals = 0): string =>
  new Intl.NumberFormat('th-TH', { maximumFractionDigits: decimals }).format(value)

function generateTextSummary(result: SolarAnalysisAdvanced): string {
  const lines: string[] = [
    '=== SolarIQ Analysis Report ===',
    '',
    `Location: ${result.address}`,
    `Coordinates: ${(result.coordinates.latitude ?? 0).toFixed(4)}, ${(result.coordinates.longitude ?? 0).toFixed(4)}`,
    '',
    '--- System Recommendation ---',
    `System Size: ${(result.panelConfig.capacityKw ?? 0).toFixed(2)} kWp`,
    `Number of Panels: ${result.panelConfig.panelsCount}`,
    `Yearly Production: ${formatNumber(result.panelConfig.yearlyEnergyDcKwh)} kWh`,
    '',
    '--- Financial Summary ---',
    `Installation Cost: ${formatCurrency(result.financialAnalysis.installationCost)}`,
    `Net Cost: ${formatCurrency(result.financialAnalysis.netCost)}`,
    `Monthly Savings: ${formatCurrency(result.financialAnalysis.monthlySavings)}`,
    `Yearly Savings: ${formatCurrency(result.financialAnalysis.yearlySavings)}`,
    `Payback Period: ${(result.financialAnalysis.paybackYears ?? 0).toFixed(1)} years`,
    `25-Year ROI: ${(result.financialAnalysis.roi25Year ?? 0).toFixed(1)}%`,
    `NPV (25-Year): ${formatCurrency(result.financialAnalysis.npvThb)}`,
    `IRR: ${(result.financialAnalysis.irrPercent ?? 0).toFixed(1)}%`,
  ]

  if (result.annualMaintenanceCost) {
    lines.push(`Annual Maintenance: ${formatCurrency(result.annualMaintenanceCost)}`)
  }

  if (result.selfConsumptionRate) {
    lines.push(`Self-Consumption Rate: ${(result.selfConsumptionRate * 100).toFixed(0)}%`)
  }

  if (result.netBillingRate) {
    lines.push(`Net Billing Rate: ${(result.netBillingRate ?? 0).toFixed(1)} THB/kWh`)
  }

  lines.push('', '--- Environmental Impact ---')

  if (result.environmentalImpact) {
    lines.push(
      `CO2 Offset/Year: ${formatNumber(result.environmentalImpact.co2OffsetTonsPerYear, 1)} tons`,
      `CO2 Offset (25-yr): ${formatNumber(result.environmentalImpact.co2OffsetTons25yr, 1)} tons`,
      `Trees Equivalent: ${formatNumber(result.environmentalImpact.treesEquivalent)}`,
      `Cars Off Road: ${formatNumber(result.environmentalImpact.carsOffRoad, 1)}`
    )
  }

  lines.push(
    '',
    '--- Solar Potential ---',
    `Max Sunshine Hours: ${formatNumber(result.solarPotential.maxSunshineHoursPerYear)} hrs/yr`,
    `Roof Area: ${(result.solarPotential.roofAreaM2 ?? 0).toFixed(0)} m2`,
    `Max Panels: ${result.solarPotential.maxPanels}`,
    `Quality: ${result.solarPotential.quality}`,
    '',
    `Electricity Rate: ${(result.electricityRate ?? 0).toFixed(2)} THB/kWh`,
    '',
    '=== Generated by SolarIQ ==='
  )

  return lines.join('\n')
}

